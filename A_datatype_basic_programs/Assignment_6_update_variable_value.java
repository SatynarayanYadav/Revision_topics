package A_datatype_basic_programs;

public class Assignment_6_update_variable_value
{
   public static void main(String[] args) 
   {
	   int a = 200;
	   
	   long l = 289;
	   
	   short s = 3879;
	   
	   String name= "satya";
	   
	   System.out.println("===Before update the variable value===");
	   System.out.println("print value of int => "+a);
	   System.out.println("print value of long => "+l);
	   System.out.println("print value of short => "+s);
	   System.out.println("print value of String => "+name);
	   
	   a=350;
	   l=400;
	   s=5000;
	   name= "satynarayan";
	   
	   System.out.println();
	   System.out.println("===After updating the variable value===");
	   System.out.println("print value of int => "+a);
	   System.out.println("print value of long => "+l);
	   System.out.println("print value of short => "+s);
	   System.out.println("print value of String => "+name);
	   

	   
   }
}

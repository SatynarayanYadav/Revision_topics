package A_datatype_basic_programs;

public class Assignment5_All_data_type 
{
   public static void main (String []args)
   {
	   int a = 90;
	   System.out.println("value of int =>"+a);
	   
	   short b = 100;
	   System.out.println("value of short =>"+b);
	   
	   byte bt = 50;
	   System.out.println("value of byte =>"+bt);
	   
	   long c = 300;
	   System.out.println("value of long =>"+c);
	   
	   float flot = 10.521f;
	   System.out.println("value of float =>"+flot);
	   
	   double dob = 100.98;
	   System.out.println("value of double =>"+dob);
	   
	   char ch = 'H';
	   System.out.println("value of char =>"+ch);
	   
	   boolean boo = true;
	   System.out.println("value of boolean =>"+boo);
	   
	   String st = "Hellow string";
	   System.out.println("value of string =>"+st);
	   
   }
}
